"""
命令行工具 - Agent Bridge CLI
"""

import argparse
import json
import sys
from pathlib import Path
from typing import Optional


def cmd_create(args):
    """创建新工具模板"""
    from .tool import Tool, ToolWrapper
    
    template_dir = Path(__file__).parent.parent / "templates"
    template_file = template_dir / f"{args.template}.py"
    
    if not template_file.exists():
        # 使用默认模板
        content = f'''"""
{args.name} - TODO: Add description

Generated by Agent Bridge
"""

from agent_bridge import Tool


@Tool(
    name="{args.name}",
    description="TODO: Add description",
    parameters={{
        # "param1": {{"type": "string", "required": True, "description": "Parameter description"}},
    }},
    tags=["{args.template}"],
)
def {args.name.replace("-", "_")}(**kwargs):
    """TODO: Implement this function"""
    pass


# Test
if __name__ == "__main__":
    result = {args.name.replace("-", "_")}()
    print(result)
'''
    else:
        content = template_file.read_text().replace("{{name}}", args.name)
    
    output_file = Path(args.output) if args.output else Path(f"{args.name}.py")
    output_file.write_text(content, encoding="utf-8")
    
    print(f"✅ Created: {output_file}")
    return 0


def cmd_convert(args):
    """转换工具格式"""
    from .converter import Converter
    
    source = Path(args.source)
    output = Path(args.output) if args.output else Path(f"./converted_{args.to_format}")
    
    if source.is_dir():
        # 批量转换
        converted = Converter.batch_convert(
            str(source),
            str(output),
            from_format=args.from_format,
            to_format=args.to_format,
        )
        print(f"✅ Converted {len(converted)} tools to {output}")
        for path in converted:
            print(f"   - {path}")
    else:
        # 单个文件转换
        print("❌ Directory source required for batch conversion")
        return 1
    
    return 0


def cmd_validate(args):
    """验证工具兼容性"""
    from .tool import ToolWrapper
    from .converter import Converter
    
    tool_path = Path(args.path)
    
    try:
        if (tool_path / "SKILL.md").exists():
            # OpenClaw skill
            tool = Converter.from_openclaw(str(tool_path))
            print(f"✅ Valid OpenClaw Skill: {tool.tool.name}")
        elif tool_path.suffix == ".py":
            # Hermes tool or standalone
            tool = Converter.from_hermes(str(tool_path))
            print(f"✅ Valid Hermes Tool: {tool.tool.name}")
        else:
            print(f"❌ Unknown format: {tool_path}")
            return 1
        
        # 验证元数据
        if not tool.tool.name:
            print("❌ Missing tool name")
            return 1
        
        if not tool.tool.description:
            print("⚠️  Missing description (recommended)")
        
        print(f"📋 Parameters: {len(tool.tool.parameters)}")
        print(f"🏷️  Tags: {tool.tool.tags}")
        
        return 0
    
    except Exception as e:
        print(f"❌ Validation failed: {e}")
        return 1


def cmd_publish(args):
    """发布工具"""
    from .converter import Converter
    
    tool_path = Path(args.path)
    platforms = args.platforms.split(",") if args.platforms else ["clawhub"]
    
    print(f"📦 Publishing to: {', '.join(platforms)}")
    
    # TODO: 实现实际发布逻辑
    for platform in platforms:
        if platform == "clawhub":
            print(f"   → ClawHub: Not yet implemented")
