def cmd_sync(args):
    """同步工具库"""
    from .openclaw import OpenClawAdapter
    from .hermes import HermesAdapter
    
    source = args.source
    dest = args.dest
    
    print(f"🔄 Syncing from {source} to {dest}")
    
    if source == "clawhub":
        adapter = OpenClawAdapter()
        skills = adapter.sync_from_clawhub(args.category)
        print(f"   Synced {len(skills)} skills")
    else:
        print(f"❌ Unknown source: {source}")
        return 1
    
    return 0


def cmd_list(args):
    """列出工具"""
    from .openclaw import OpenClawAdapter
    from .hermes import HermesAdapter
    
    if args.format == "openclaw":
        adapter = OpenClawAdapter()
        tools = adapter.list_skills()
    elif args.format == "hermes":
        adapter = HermesAdapter()
        tools = adapter.list_tools()
    else:
        print("❌ Unknown format. Use 'openclaw' or 'hermes'")
        return 1
    
    if not tools:
        print("📭 No tools found")
        return 0
    
    print(f"📦 Found {len(tools)} tools:\n")
    for tool in tools:
        print(f"  • {tool['name']}")
        print(f"    {tool['description'][:60]}..." if len(tool['description']) > 60 else f"    {tool['description']}")
        if tool.get('tags'):
            print(f"    Tags: {', '.join(tool['tags'])}")
        print()
    
    return 0


def cmd_run(args):
    """运行工具"""
    import subprocess
    
    tool_name = args.name
    tool_args = json.loads(args.args) if args.args else {}
    
    # 查找工具
    from .openclaw import OpenClawAdapter
    
    adapter = OpenClawAdapter()
    result = adapter.run_skill(tool_name, tool_args)
    
    print(json.dumps(result, indent=2, ensure_ascii=False))
    return 0


def main():
    """CLI 入口点"""
    parser = argparse.ArgumentParser(
        prog="agent-bridge",
        description="🌉 Agent Bridge - 让 OpenClaw 和 Hermes Agent 共享技能/工具",
    )
    
    subparsers = parser.add_subparsers(dest="command", help="Commands")
    
    # create 命令
    create_parser = subparsers.add_parser("create", help="创建新工具模板")
    create_parser.add_argument("name", help="工具名称")
    create_parser.add_argument("-t", "--template", default="general", help="模板类型")
    create_parser.add_argument("-o", "--output", help="输出文件路径")
    create_parser.set_defaults(func=cmd_create)
    
    # convert 命令
    convert_parser = subparsers.add_parser("convert", help="转换工具格式")
    convert_parser.add_argument("source", help="源目录")
    convert_parser.add_argument("--from", dest="from_format", required=True, help="源格式 (openclaw/hermes)")
    convert_parser.add_argument("--to", dest="to_format", required=True, help="目标格式 (openclaw/hermes)")
    convert_parser.add_argument("-o", "--output", help="输出目录")
    convert_parser.set_defaults(func=cmd_convert)
    
    # validate 命令
    validate_parser = subparsers.add_parser("validate", help="验证工具兼容性")
    validate_parser.add_argument("path", help="工具路径")
    validate_parser.set_defaults(func=cmd_validate)
    
    # publish 命令
    publish_parser = subparsers.add_parser("publish", help="发布工具")
    publish_parser.add_argument("path", help="工具路径")
    publish_parser.add_argument("-p", "--platforms", help="发布平台 (逗号分隔)")
    publish_parser.set_defaults(func=cmd_publish)
    
    # sync 命令
    sync_parser = subparsers.add_parser("sync", help="同步工具库")
    sync_parser.add_argument("--source", required=True, help="源 (clawhub/hermes)")
    sync_parser.add_argument("--dest", default="local", help="目标")
    sync_parser.add_argument("-c", "--category", help="分类过滤")
    sync_parser.set_defaults(func=cmd_sync)
    
    # list 命令
    list_parser = subparsers.add_parser("list", help="列出工具")
    list_parser.add_argument("-f", "--format", default="openclaw", help="工具格式")
    list_parser.set_defaults(func=cmd_list)
    
    # run 命令
    run_parser = subparsers.add_parser("run", help="运行工具")
    run_parser.add_argument("name", help="工具名称")
    run_parser.add_argument("-a", "--args", help="JSON 格式参数")
    run_parser.set_defaults(func=cmd_run)
    
    args = parser.parse_args()
    
    if not args.command:
        parser.print_help()
        return 0
    
    return args.func(args)


if __name__ == "__main__":
    sys.exit(main())
